    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É –ø–æ–º–æ—â–∏, –æ—Ç–≤–µ—á–∞—è –Ω–∞ –∫–æ–ª–±–µ–∫-–∑–∞–ø—Ä–æ—Å.

    :param callback_query: –û–±—ä–µ–∫—Ç –∫–æ–ª–±–µ–∫-–∑–∞–ø—Ä–æ—Å–∞, –ø–æ–ª—É—á–µ–Ω–Ω–æ–≥–æ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    await callback_query.answer()  # –£–±–∏—Ä–∞–µ–º "–∫—Ä—É—Ç–∏–ª–∫—É" (–∏–Ω–¥–∏–∫–∞—Ç–æ—Ä –∑–∞–≥—Ä—É–∑–∫–∏)
